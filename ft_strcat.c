char    *ft_strcat(char *s1, const char *s2)
{
	char    *cut1;
	char    *cut2;

	cut1 = s1;
	cut2 = (char *)s2;
	while (*cut1 != '\0')
		cut1++;
	while (*cut2 != '\0')
	{
		*cut1 = *cut2;
		cut1++;
		cut2++;
	}
	*cut1 = '\0';
	return (s1);
}
